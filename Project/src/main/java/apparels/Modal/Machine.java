package apparels.Modal;

public class Machine {
	private int id;
	private String mname;
	private String mmodel;
	private String mhandler;
	private String mcats;
	private String mcolor;
	private String added;
	private String modify;
	private String mcondition;
	
	public Machine() {
		super();
	}

	public String getMcondition() {
		return mcondition;
	}

	public void setMcondition(String mcondition) {
		this.mcondition = mcondition;
	}

	public String getMcats() {
		return mcats;
	}

	public void setMcats(String mcats) {
		this.mcats = mcats;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMname() {
		return mname;
	}

	public void setMname(String mname) {
		this.mname = mname;
	}

	public String getMmodel() {
		return mmodel;
	}

	public void setMmodel(String mmodel) {
		this.mmodel = mmodel;
	}

	public String getMhandler() {
		return mhandler;
	}

	public void setMhandler(String mhandler) {
		this.mhandler = mhandler;
	}

	public String getMcolor() {
		return mcolor;
	}

	public void setMcolor(String mcolor) {
		this.mcolor = mcolor;
	}

	public String getAdded() {
		return added;
	}

	public void setAdded(String added) {
		this.added = added;
	}

	public String getModify() {
		return modify;
	}

	public void setModify(String modify) {
		this.modify = modify;
	}
	
	

}
